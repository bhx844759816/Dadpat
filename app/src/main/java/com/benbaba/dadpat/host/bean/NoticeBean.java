package com.benbaba.dadpat.host.bean;

/**
 * 公告对象
 */
public class NoticeBean {
    private String noticeContent;
    private String noticeId;
    private String noticeBegin;
    private String noticeEnd;
    private String notcieTitle;
    private String noticeTime;

    public String getNoticeContent() {
        return noticeContent;
    }

    public void setNoticeContent(String noticeContent) {
        this.noticeContent = noticeContent;
    }

    public String getNoticeId() {
        return noticeId;
    }

    public void setNoticeId(String noticeId) {
        this.noticeId = noticeId;
    }

    public String getNoticeBegin() {
        return noticeBegin;
    }

    public void setNoticeBegin(String noticeBegin) {
        this.noticeBegin = noticeBegin;
    }

    public String getNoticeEnd() {
        return noticeEnd;
    }

    public void setNoticeEnd(String noticeEnd) {
        this.noticeEnd = noticeEnd;
    }

    public String getNotcieTitle() {
        return notcieTitle;
    }

    public void setNotcieTitle(String notcieTitle) {
        this.notcieTitle = notcieTitle;
    }

    public String getNoticeTime() {
        return noticeTime;
    }

    public void setNoticeTime(String noticeTime) {
        this.noticeTime = noticeTime;
    }
}
